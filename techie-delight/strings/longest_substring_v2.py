# Given a string , find longest substring with non repetitive characters"
# 
# Examples:
#
# string = "abcabcbb", output = "abc", length = 3
# string = "bbbbbbb", output = "b", length = 1
# string = "pwwkew", output ="wke", length = 3
# string = "dvdf", output = "vdf", length = 3


def get_substrings(s, seen_list):
    seen = ""
    if s is "":
        seen_list.append(0)
        return

    index = 0
    updated_seen_list = False
    for c in s:
        if c not in seen:
            seen = seen + c
            index = index + 1        
        else:
            seen_list.append(len(seen))
            update_seen_list = True
            s = s[1:]
            get_substrings(s,seen_list)
            return
    if updated_seen_list is False:
        seen_list.append(len(seen))
    return
            
def longest_substring(s):
    seen_list = []
    get_substrings(s, seen_list)

    max_ = 0
    max_so_far = 0

    for i in range(0,len(seen_list)): 
        max_ = seen_list[i]
        if max_ > max_so_far:
            max_so_far = max_
    
    return max_so_far


